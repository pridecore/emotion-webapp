async def run_affirmation_scheduler(bot):
    while True:
        try:
            # Поточний час у Києві
            now = datetime.now(ZoneInfo("Europe/Kyiv")).strftime("%H:%M")
            file_path = "data/affirmation_schedule.json"

            if os.path.exists(file_path):
                with open(file_path, "r", encoding="utf-8") as f:
                    try:
                        schedule = json.load(f)
                        if not isinstance(schedule, dict):
                            schedule = {}
                    except json.JSONDecodeError:
                        schedule = {}

                for user_id_str, time_str in schedule.items():
                    if time_str == now:
                        affirmation = random.choice(affirmations)
                        try:
                            await bot.send_message(chat_id=int(user_id_str), text=affirmation)
                        except Exception as e:
                            print(f"❌ Не вдалося надіслати афірмацію користувачу {user_id_str}: {e}")
        except Exception as outer:
            print(f"❗ Помилка в scheduler: {outer}")

        await asyncio.sleep(60)  # раз на хвилину